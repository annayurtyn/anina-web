// ─── helpers ───
function i18nGet(lang, key) {
  return key.split('.').reduce(function(o, k) { return o && o[k]; }, TRANSLATIONS[lang]);
}

function setLanguage(lang) {
  if (!TRANSLATIONS[lang]) return;
  document.documentElement.lang = lang;
  localStorage.setItem('aninka-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var val = i18nGet(lang, el.getAttribute('data-i18n'));
    if (val !== undefined) el.innerHTML = val;
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    var val = i18nGet(lang, el.getAttribute('data-i18n-ph'));
    if (val !== undefined) el.placeholder = val;
  });

  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

// ─── init ───
(function() {
  var lang = localStorage.getItem('aninka-lang') || 'cs';
  setLanguage(lang);

  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.addEventListener('click', function() {
      setLanguage(this.getAttribute('data-lang'));
    });
  });
})();
